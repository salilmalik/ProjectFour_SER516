package server.model;

public class Sensitivity {
    private float hot;
    private float cold;
    private float dust;

    public float getHot() {
        return hot;
    }

    public void setHot(float hot) {
        this.hot = hot;
    }

    public float getCold() {
        return cold;
    }

    public void setCold(float cold) {
        this.cold = cold;
    }

    public float getDust() {
        return dust;
    }

    public void setDust(float dust) {
        this.dust = dust;
    }
}
